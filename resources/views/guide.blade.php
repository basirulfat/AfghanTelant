        برای مثال در آمریکا و انگلیس معمولاً از عکس در رزومه استفاده نمی‌شود،
        اما در کشورهایی مانند فرانسه، بلژیک و آلمان مشکلی از این بابت وجود
        ندارد. با این حال، امروزه با گسترش اینترنت و شبکه‌های اجتماعی مانند
        لینکدین، استفاده از عکس در رزومه به امری اختیاری تبدیل شده است. اگر در
