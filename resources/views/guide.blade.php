        همکاری در مراکز علمی، آموزشی و دانشگاهی استفاده می‌کنند. این نوع رزومه
        نیز مانند رزومه کاری می‌تواند اطلاعات مربوط به سوابق تحصیلی، سوابق
        استخدامی و شغلی را شامل شود، اما در کنار آن سوابق کارهای تحقیقاتی و
        تدریس در دانشگاه‌ها، سابقه انتشار مقاله، جوایز و دستاوردهای علمی و
        بورسیه‌های تحصیلی را نیز شامل می‌شود. در این نوع رزومه، بیشتر باید روی
        سوابق علمی و تجربه کار در مراکز آموزشی تمرکز کرد.
      </p>

