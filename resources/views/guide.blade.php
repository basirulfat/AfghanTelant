      <p>
        رزومه کارآموزی سندی است که در آن مهم‌ترین مهارت‌ها، دستاوردها و همچنین
        سوابق کاری دانشجو آورده می‌شود تا بتواند نظر کارفرمایان و کارشناسان
        استخدام را جلب کند. این رزومه حاوی اطلاعات مهمی از جمله تحصیلات، سابقه
        کارآموزی و کاری، موفقیت‌ها و مهارت‌ها خواهد بود. رزومه کارآموزی کمی با
        رزومه کاری فرق دارد و عمده تفاوت آن در این است که، که در رزومه کارآموزی
        کارجو این اجازه را دارد که از موفقیت‌ها ودستاوردهای خود در دانشگاه
        بگویید و کارهای پاره وقت خود را برجسته کند.
      </p>
      <h2>رزومه آنلاین</h2>
      <p>
        رزومه آنلاین کمی با آنچه شما به عنوان سند بازاریابی می‌سازید، فرق دارد.
        در رزومه کاری خود شما تنظیم کننده قالب و بایدها و نبایدهای رزومه هستید،
        اما در رزومه آنلاین این شرکت‌ها هستید که تعیین می‌کنند چه بخشی در رزومه
        باید باشد و کدام بخش‌ها باید پر شوند. رزومه‌های آنلاین همان فرم‌هایی
        هستند که در صفحه فرصت‌های شغلی کسب و کارها مشاهده می‌کنید و شامل تمام
        موارد موجود در رزومه می‌شوند. کارجو برای استخدام در شرکتی، به صفحه
        فرصت‌های شغلی آن شرکت در وب‌سایت سر می زند و برای هر موقعیت شغلی رزومه
        آنلاین پر می‌کند.
      </p>

