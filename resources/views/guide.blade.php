        دستاوردها، همچنین شرح حالی از فرد است. این مدل رزومه را می‌توان
        کامل‌ترین نوع رزومه نامید، چرا که نه مانند رزومه تحصیلی سابقه کاری کاری
        در آن کم است و نه مانند رزومه تحصیلی تنها محدود به مدارک تحصیلی می‌شود.
      </p>

