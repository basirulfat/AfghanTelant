        بودن رزومه‌ها، آن‌ها چگونه باید سوابق شغلی خود را وارد کنند. همین موضوع
        دلیلی شد تا ما این مطلب آموزشی را برایتان تهیه کنیم. در ادامه شما
        می‌توانید یک نمونه سوابق شغلی بر اساس موارد گفته شده در بالا را مشاهده
        کنید (البته توصیه ما این است که مطلب مرتبط ساخت رزومه در ایران تلنت را
        یکبار پیش از تکمیل رزومه خود مطالعه نمایید). پس از ثبت نام در ایران تلنت
        از طریق منوی سمت راست در بالای صفحه، وارد اکانت خود شوید. پس از ورود به
        سایت، وارد داشبورد کاربری خواهید شد. حال از طریق منو بالای صفحه، بر روی
        گزینه My CV سپس روی Update CV کلیک کنید تا وارد مرحله ساخت و ویرایش
        رزومه خود شوید. پس از ورود به رزومه و وارد کردن اطلاعات شخصی مانند نام و
        نام‌خانوادگی، سن، شماره تماس و …، زمان آن است که سوابق شغلی خود را در
        ررزومه وارد نمایید.
      </p>

      <h2>موارد مهم در نوشتن سوابق شغلی</h2>
      <p>
        در ادامه هر یک از این موارد را با هم بررسی می‌کنیم (پُر کردن فیلدهایی که
        با ستاره قرمز رنگ مشخص شده‌اند، اجباری است): Job Title: در اینجا باید
        عنوان شغلی خود را به انگلیسی بنویسید مانند Sales Expert یا Marketing
        Manager Employment Type: در اینجا نوع همکاری خود را با آن مجموعه از بین
        گزینه‌های Full Time، Part Time، Internship یا Freelance انتخاب کنید.
        Company Name: در اینجا باید نام مجموعه یا وب سایت آن مجموعه را درج کنید.
        Company Size: در این قسمت می‌توانید سایز شرکت را انتخاب کنید. Job Role:
        در این قسمت باید حیطه‌ای که در آن فعالیت می‌کردید را مشخص کنید، مانند
        Sales & Marketing یا Accounting. Industry: صنعتی که شرکت در آن فعال بوده
        است را باید انتخاب کنید، مانند Banking یا HR & Recruitment. Seniority:
        در این قسمت باید میزان ارشد بودن خود را از بین موارد انتخاب کنید مانند
        Manager یا Experienced Professional. Location: در این قسمت می‌توانید
        کشور، استان و شهری که شرکت در آن قرار داشته است را وارد کنید. بازه زمانی
        اشتغال (From – To): در این قسمت مشخص می‌کنید که از چه تاریخی تا چه
        تاریخی (میلادی) در محل کار مورد اشاره حضور داشته‌اید. در این قسمت یک
        مبدل تاریخ شمسی به میلادی برای سهولت کار شما در نظر گرفته شده است (با
        نام Date Converter). ضمن اینکه اگر در حال حاضر در شرکت مربوطه مشغول به
        کار هستید، با فعال کردن گزینه I Currently Work Here این موضوع را به
        کارفرما اطلاع دهید. Description: این قسمت همان شرح وظایفی است که در بالا
        درباره آن صحبت کردیم که در ادامه نمونه‌ای از نوشتن شرح شغل در رزومه را
        توضیح خواهیم داد: اگر به یاد داشته باشید در بالا گفتیم که در نوشتن سابقه
        شغلی یا همان شرح شغل باید به ۴ مورد اشاره کنید: معرفی کوتاه از مجموعه‌ای
        که در آن فعالیت می‌کرده‌اید (یا در آن مشغول به کار هستید) معرفی شخصی که
        به او گزارش می‌دادید مهم‌ترین مسئولیت‌هایی که بر عهده داشتید مهم‌ترین
        دستاوردهایی که در دوران کاری خود در آن مجموعه به دست آورده‌اید
      </p>

      <h2>رزومه کارآموزی</h2>
      <p>
        رزومه کارآموزی سندی است که در آن مهم‌ترین مهارت‌ها، دستاوردها و همچنین
        سوابق کاری دانشجو آورده می‌شود تا بتواند نظر کارفرمایان و کارشناسان
        استخدام را جلب کند. این رزومه حاوی اطلاعات مهمی از جمله تحصیلات، سابقه
        کارآموزی و کاری، موفقیت‌ها و مهارت‌ها خواهد بود. رزومه کارآموزی کمی با
        رزومه کاری فرق دارد و عمده تفاوت آن در این است که، که در رزومه کارآموزی
        کارجو این اجازه را دارد که از موفقیت‌ها ودستاوردهای خود در دانشگاه
        بگویید و کارهای پاره وقت خود را برجسته کند.
      </p>
      <h2>جمع‌بندی</h2>
