        از کلمات کلیدی مرتبط و مهم استفاده کنید. پس از نوشتن سوابق شغلی، یکبار
        آن‌ها را بخوانید. این بار سعی کنید کلمات کلیدی مهمی که در موقعیت
        شغلی‌تان مهم هستند را به آن‌ها اضافه کنید. این‌ کلمات کلیدی، توصیف‌کننده
        رزومه و سابقه شما هستند. از طرف دیگر، وجود این کلمات کلیدی در رزومه شما
